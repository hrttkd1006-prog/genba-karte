def match_faq(text: str) -> str | None:
    """テキストに含まれるキーワードでFAQマッチ。"""
    if not text:
        return None
    normalized = text.strip()
    for keywords, response in FAQ_ENTRIES:
        for kw in keywords:
            if kw in normalized:
                return response
    return None
